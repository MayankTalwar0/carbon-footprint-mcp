def lookup_stationary(fuel_name):
    """Look up stationary combustion factors by fuel name (case-insensitive)."""
    key = fuel_name.strip()
    if key in STATIONARY_COMBUSTION:
        return STATIONARY_COMBUSTION[key]
    lower = key.lower()
    for k, v in STATIONARY_COMBUSTION.items():
        if k.lower() == lower:
            return v
    return None


def lookup_mobile_co2(fuel_name):
    """Look up mobile combustion CO2 factor by fuel name."""
    key = fuel_name.strip()
    if key in MOBILE_CO2:
        return MOBILE_CO2[key]
    lower = key.lower()
    for k, v in MOBILE_CO2.items():
        if k.lower() == lower:
            return v
    return None


def lookup_egrid(subregion):
    """Look up eGRID electricity factors by subregion acronym."""
    key = subregion.strip()
    if key in EGRID:
        return EGRID[key]
    upper = key.upper()
    if upper in EGRID:
        return EGRID[upper]
    # Case-insensitive fallback
    lower = key.lower()
    for k, v in EGRID.items():
        if k.lower() == lower:
            return v
    return None


def lookup_waste(material_name):
    """Look up waste disposal emission factors by material name."""
    lower = material_name.strip().lower()
    for item in WASTE:
        if item["material"].lower() == lower:
            return item
    return None


def lookup_transportation(vehicle_type):
    """Look up Scope 3 transportation factors by vehicle type."""
    lower = vehicle_type.strip().lower()
    for item in TRANSPORTATION:
        if item["vehicle_type"].lower() == lower:
            return item
    return None


def lookup_business_travel(vehicle_type):
    """Look up business travel / commuting factors by vehicle type."""
    lower = vehicle_type.strip().lower()
    for item in BUSINESS_TRAVEL:
        if item["vehicle_type"].lower() == lower:
            return item
    # Partial match
    for item in BUSINESS_TRAVEL:
        if lower in item["vehicle_type"].lower():
            return item
    return None


def lookup_gwp(gas_name):
    """Look up 100-year GWP by gas name or formula."""
    if gas_name in GWP:
        return GWP[gas_name]["gwp_100yr"]
    lower = gas_name.strip().lower()
    for k, v in GWP.items():
        if k.lower() == lower or (v["formula"] and v["formula"].lower() == lower):
            return v["gwp_100yr"]
    return None


def lookup_refrigerant(ashrae_number):
    """Look up blended refrigerant GWP by ASHRAE number."""
    key = ashrae_number.strip()
    if key in REFRIGERANTS:
        return REFRIGERANTS[key]
    # Try with R- prefix
    if not key.startswith("R-"):
        return REFRIGERANTS.get(f"R-{key}")
    return None


def list_egrid_subregions():
    """Return list of all eGRID subregion acronyms and names."""
    return {k: v["name"] for k, v in EGRID.items()}


def list_fuels():
    """Return list of all stationary combustion fuel names."""
    return list(STATIONARY_COMBUSTION.keys())


def list_waste_materials():
    """Return list of all waste material names."""
    return [item["material"] for item in WASTE]
